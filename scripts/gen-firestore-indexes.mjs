#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────
// scripts/gen-firestore-indexes.mjs
// Generates firestore.indexes.json + scripts/setup-vector-indexes.sh
// directly from the single source of truth: lib/scam-intel/firestore-schema.ts
//
// The schema file has zero imports, so we transpile it in-memory with the
// TypeScript compiler (already a dependency) and import the result — no
// build step, no duplication.
//   Usage: node scripts/gen-firestore-indexes.mjs
// ─────────────────────────────────────────────────────────────────

import { readFileSync, writeFileSync } from 'node:fs'
import { pathToFileURL } from 'node:url'
import path from 'node:path'
import os from 'node:os'
import ts from 'typescript'

const root = process.cwd()
const schemaPath = path.join(root, 'lib', 'scam-intel', 'firestore-schema.ts')

const source = readFileSync(schemaPath, 'utf8')
const { outputText } = ts.transpileModule(source, {
  compilerOptions: { module: 'ESNext', target: 'ES2022' },
})

const tmp = path.join(os.tmpdir(), `fsschema-${Date.now()}.mjs`)
writeFileSync(tmp, outputText)
const mod = await import(pathToFileURL(tmp).href)

// 1. Composite indexes → firestore.indexes.json
const indexes = mod.toFirestoreIndexesJson()
const outPath = path.join(root, 'firestore.indexes.json')
writeFileSync(outPath, JSON.stringify(indexes, null, 2) + '\n')
console.log(`✓ wrote firestore.indexes.json (${indexes.indexes.length} composite indexes)`)

// 2. Vector indexes → setup-vector-indexes.sh (created via gcloud)
const vectorCmds = mod.vectorIndexCommands()
const shPath = path.join(root, 'scripts', 'setup-vector-indexes.sh')
const sh = [
  '#!/usr/bin/env bash',
  '# Auto-generated by gen-firestore-indexes.mjs — do not edit by hand.',
  '# Creates Firestore vector indexes (KNN) via gcloud.',
  'set -euo pipefail',
  '',
  ...vectorCmds,
  '',
].join('\n')
writeFileSync(shPath, sh)
console.log(`✓ wrote scripts/setup-vector-indexes.sh (${vectorCmds.length} vector indexes)`)

// 3. Summary
console.log('\nCollections:')
for (const c of mod.COLLECTIONS) {
  console.log(`  - ${c.name}${c.vectorIndex ? ' [vector]' : ''}${c.ttlField ? ` [ttl:${c.ttlField}]` : ''}`)
}
